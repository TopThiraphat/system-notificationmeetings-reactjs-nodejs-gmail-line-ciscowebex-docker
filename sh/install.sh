#!/bin/bash

#################################################################################################
## create Network
echo "-----------------[[ The system is installing Network Container ]]----------------

"
docker network create \
  --driver=bridge \
  --subnet=10.5.0.0/27 \
  --ip-range=10.5.0.1/27 \
  --gateway=10.5.0.1 \
  webapp_network

echo "

-----------------------------------------------------------------------


"
#################################################################################################
echo "-----------------[[ The system is Webapp ]]----------------
"

cd ..

docker-compose -f docker-compose.webapp.yml -p "webapp" up -d

echo "

-----------------------------------------------------------------------


"